function Navbar() {
  const navigate = useNavigate();
  const dispatch = useDispatch();

  const user = useSelector(selectUser);
  const isAdmin = useSelector(selectIsAdmin);

  async function handleLogout() {
    try {
      await dispatch(logoutUserThunk()).unwrap();

      dispatch(resetCartState());

      navigate("/login");
    } catch (error) {
      console.log(error || "Logout failed");
    }
  }

  return (
    <nav className="navbar">
      <NavLink to="/" className="logo-link">
        <img src="/final.png" alt="MARKIT" className="logo-img" />
      </NavLink>

      <div className="nav-slogan">
        <span className="slogan-highlight">Markit</span> - just mark it and get it
      </div>

      <div className="nav-links">
        <NavLink to="/">{icons.products}<span>Products</span></NavLink>

        <NavLink to="/cart">{icons.cart}<span>Cart</span></NavLink>

        {user ? (
          <>
            <NavLink to="/orders">{icons.orders}<span>Orders</span></NavLink>

            {isAdmin && <NavLink to="/admin">{icons.admin}<span>Admin</span></NavLink>}

            <span className="nav-divider" />

            <span className="nav-user">{icons.user} {user.name}</span>

            <button className="logout-btn" onClick={handleLogout}>
              {icons.logout}<span>Logout</span>
            </button>
          </>
        ) : (
          <>
            <span className="nav-divider" />
            <NavLink to="/login" className="nav-login-link">{icons.login}<span>Login</span></NavLink>
            <NavLink to="/register" className="nav-register-link">{icons.register}<span>Register</span></NavLink>
          </>
        )}
      </div>
    </nav>
  );
}

export default Navbar;